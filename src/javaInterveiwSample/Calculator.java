package javaInterveiwSample;

public class Calculator {
	double num1=5, num2=10;

	void addTwoNumbers() {
		double result = num1 + num2;
		System.out.println(result);
	}

	void subTwoNumbers() {
		double result = num1 - num2;
		System.out.println(result);
	}

	void multiplyTwoNumbers() {
		double result = num1 * num2;
		System.out.println(result);
	}

	void divideTwoNumbers() {
		double result = num1 / num2;
		System.out.println(result);
	}
}
